def is_numeric_string(s):
    """
    Check if a string represents a numeric value, including negative, decimal,
    and percentage values.
    """
    try:
        float(s)  # Try converting the string to a float
        return True
    except ValueError:
        pass  # If conversion fails, proceed to check for percentages

    if s.endswith('%'):
        try:
            float(s.rstrip('%'))  # Remove the '%' and try converting
            return True
        except ValueError:
            pass

    return False


